#include "libft.h"

t_u32	ft_memcmp(const void *str1, const void *str2, t_size n)
{
	t_size	c;
	t_u32	*s1;
	t_u32	*s2;

	c = 0;
	s1 = (t_u32 *)str1;
	s2 = (t_u32 *)str2;
	while (c < n && s1[c] == s2[c])
	{
		c++;
	}
	return (s1[c] - s2[c]);
}
