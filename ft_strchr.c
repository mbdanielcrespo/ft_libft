#include "libft.h"

char	*ft_strchr(const char *str, t_u8 ch)
{
	t_u32	c;

	c = 0;
	while (str[c] != ch && str[c] != '\0')
	{
		c++;
	}
	if (str[c] == (char)ch)
	{
		return ((char *)&str[c]);
	}
	return (NULL);
}
