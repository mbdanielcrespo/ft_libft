#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*aux;
	t_i32	c;

	aux = lst;
	c = 0;
	if (!aux)
		return (NULL);
	while (c <= ft_lstsize(aux))
	{
		aux = aux->next;
		c++;
	}
	return (aux);
}
